  public void cargarTxt(String valor) {
        String mostrar = "SELECT nro_fact_ventas,nro_estable_ventas,nro_timbra_ventas FROM ventas_facturacion_surc_26 WHERE  nro_estable_ventas ='" + valor + "'  order by  nro_fact_ventas desc";

        try {

            Connection cn = conectar.getInstance().getConnection();
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(mostrar);
            if (rs.next()) {
                String c = rs.getString(1);
                String timbrado = rs.getString(3);
                if (c == null) {
                    txt_nro_fact.requestFocus();
                } else {

                    int j = Integer.parseInt(c);
                    System.out.println(j);
                    GenerarNumero gen = new GenerarNumero();
                    gen.generar(j);
                    txt_nro_fact.setText(gen.serie());
                    txt_nro_timbra.setText(timbrado);

                }
            }

            conectar.getInstance().closeConnection(cn);
        } catch (SQLException ex) {
            Logger.getLogger(parametro_cambio_26.class
                    .getName()).log(Level.SEVERE, null, ex);
        }

    }
