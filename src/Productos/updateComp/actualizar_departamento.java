    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            String id = txt_id.getText();
            String des = txtdes.getText();
            String descto = txt_descuento.getText();
            String lucro_uno = txt_lucro_uno.getText();
            String lucro_dos = txt_lucro_dos.getText();
            String sql = "UPDATE depar_productos SET des_dep = '" + des
                    + "',descto_dep ='" + descto
                    + "',por_uno ='" + lucro_uno
                    + "',por_dos ='" + lucro_dos
                    + "' WHERE cod_dep = '" + id + "'";
            try {
                Connection cn = conectar.getInstance().getConnection();

                PreparedStatement pst = cn.prepareStatement(sql);
                pst.executeUpdate();
                conectar.getInstance().closeConnection(cn);

                JOptionPane.showMessageDialog(null, "Actualizado");
                this.dispose();

            } catch (SQLException | HeadlessException e) {
                JOptionPane.showMessageDialog(null, e);
            }

        } catch (Exception e) {
        }
        Departamentos_Principal.btn_cargar_datos.doClick();

    }//GEN-LAST:event_jButton1ActionPerformed

