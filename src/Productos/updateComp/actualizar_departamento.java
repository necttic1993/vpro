    void BuscarProductoEditarA(String cod) {

        try {

            String id = "", des = "", tipo = "", lucro_uno = "", lucro_dos = "";
            Connection cn = conectar.getInstance().getConnection();

            String cons = "select * from depar_productos WHERE cod_dep='" + cod + "'";
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(cons);
            while (rs.next()) {
                id = rs.getString(1);
                des = rs.getString(2);
                tipo = rs.getString(3);
                lucro_uno = rs.getString(4);
                lucro_dos = rs.getString(5);
            }

            txt_id.setText(id);
            txtdes.setText(des);
            txt_descuento.setText(tipo);
            txt_lucro_uno.setText(lucro_uno);
            txt_lucro_dos.setText(lucro_dos);
            conectar.getInstance().closeConnection(cn);

        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }

    }

