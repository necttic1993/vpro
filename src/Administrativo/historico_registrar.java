    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String sql = "";
        String cod = txtdes.getText();
        String tipo = (String) cb_tipo.getSelectedItem();
        String clase =(String) cb_class.getSelectedItem();

        sql = "INSERT INTO historicos (des_his,tipo_des,class_his) VALUES (?,?,?)";
        try {
            Connection cn = conectar.getInstance().getConnection();

            PreparedStatement pst = cn.prepareStatement(sql);
            pst.setString(1, cod);
            pst.setString(2, tipo);
            pst.setString(3, clase);

            int n = pst.executeUpdate();
            if (n > 0) {
                JOptionPane.showMessageDialog(null, "Registro Guardado ");
                txtdes.setText("");
                cb_tipo.setSelectedItem("");
            }
            conectar.getInstance().closeConnection(cn);

        } catch (SQLException ex) {
            Logger.getLogger(historico_registrar.class.getName()).log(Level.SEVERE, null, ex);
        }
        historicos_gastos.btn_cargar_datos.doClick();
    }//GEN-LAST:event_jButton1ActionPerformed

