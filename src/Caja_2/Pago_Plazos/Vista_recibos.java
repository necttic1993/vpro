    void cargar(String valor) {
        String mostrar = "SELECT * FROM pago_credito_detalle WHERE  nro_pago='" + valor + "'";
        String[] titulos = {"Banco", "Número", "Fecha", "Importe"};
        String[] Registros = new String[7];
        model = new DefaultTableModel(null, titulos);

        try {
            Connection cn = conectar.getInstance().getConnection();

            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(mostrar);
            while (rs.next()) {
                Registros[0] = rs.getString("nom_banco");
                Registros[1] = rs.getString("nro_cheq");
                Registros[2] = rs.getString("fecha");
                Registros[3] = rs.getString("importe");

                model.addRow(Registros);
            }
            tb_detalles.setModel(model);

            tb_detalles.getColumnModel().getColumn(0).setPreferredWidth(100);
            tb_detalles.getColumnModel().getColumn(1).setPreferredWidth(80);
            tb_detalles.getColumnModel().getColumn(2).setPreferredWidth(80);
            tb_detalles.getColumnModel().getColumn(3).setPreferredWidth(80);
            conectar.getInstance().closeConnection(cn);

        } catch (SQLException ex) {
            Logger.getLogger(Vista_recibos.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    void cargarTxt(String valor) {
        String mostrar = "SELECT * FROM pago_credito WHERE  nro_pago='" + valor + "'";

        try {
            Connection cn = conectar.getInstance().getConnection();

            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(mostrar);
            while (rs.next()) {
                txt_nro_venta.setText(rs.getString(2));
                id_cliente_cre.setText(rs.getString(3));
                txt_nom_cli_cred.setText(rs.getString(4));
                chk_contado.setText(rs.getString(5));
                chk_plazo.setText(rs.getString(6));
                chk_efectivo.setText(rs.getString(7));
                chk_cheque.setText(rs.getString(8));
                lbl_total_efect.setText(rs.getString(9));
                lbl_total_cheque.setText(rs.getString(10));
                lbl_import_total.setText(rs.getString(11));
                txt_almacen.setText(rs.getString(12));
                txt_venta_cre_saldo.setText(rs.getString(13));
                txt_monto_total.setText(rs.getString(14));
                txt_nro_fact.setText(rs.getString(15));
                lbl_serie_pago.setText(rs.getString(1));

            }
            conectar.getInstance().closeConnection(cn);

        } catch (SQLException ex) {
            Logger.getLogger(Vista_recibos.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

